const dicts: Record<Lang, Dict> = { en, tr, es, pt, zh, it };

const RTL: Lang[] = [];

interface Ctx {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (k: string) => string;
  dir: "ltr" | "rtl";
}
const I18n = createContext<Ctx | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");
  useEffect(() => {
    const stored = (typeof localStorage !== "undefined" && localStorage.getItem("hl_lang")) as Lang | null;
    if (stored && dicts[stored]) { setLangState(stored); return; }
    if (typeof navigator !== "undefined") {
      const device = navigator.language?.slice(0, 2).toLowerCase() as Lang;
      if (dicts[device]) setLangState(device);
    }
  }, []);
  useEffect(() => {
    if (typeof document !== "undefined") {
      document.documentElement.lang = lang;
      document.documentElement.dir = RTL.includes(lang) ? "rtl" : "ltr";
    }
  }, [lang]);
  const setLang = (l: Lang) => { try { localStorage.setItem("hl_lang", l); } catch {} setLangState(l); };
  const t = useMemo(() => (k: string) => (dicts[lang] ?? en)[k] ?? en[k] ?? k, [lang]);
  const dir = RTL.includes(lang) ? "rtl" : "ltr";
  return <I18n.Provider value={{ lang, setLang, t, dir }}>{children}</I18n.Provider>;
}

export function useI18n() {
  const c = useContext(I18n);
  if (!c) throw new Error("useI18n outside provider");
  return c;
}
